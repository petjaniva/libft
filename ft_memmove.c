#include "libft.h"
#include <stdlib.h>

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	size_t	i;

	if (!n || (!dest && !src))
		return (dest);
	if (dest > src)
	{
		i = n;
		while (i > 0)
		{
			((char *)dest)[i - 1] = ((char *)src)[i - 1];
			--i;
		}
		return (dest);
	}
	else
		return (ft_memcpy(dest, src, n));
}
